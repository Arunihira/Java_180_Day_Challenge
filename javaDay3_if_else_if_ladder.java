

public class javaDay3_if_else_if_ladder
{
    public static void main(String[] args) 
    {
        int marks=78;
        if(marks>=90)
        {
            System.out.println("Grade A");
        }
        else if(marks>=70)
        {
            System.out.println("Grade B");
        }
        else if(marks>=50)
        {
            System.out.println("Grade c");
        }
        else 
        {
            System.out.println("Fail");
        }
    }
}